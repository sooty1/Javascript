// The === Strict Equality Operator  cares about types and values
console.log(1 === 1);
console.log( 1 === "1"); 
console.log( "z" === "z");
console.log( true === true);
console.log( false === false);
console.log( 0 === "");
console.log( undefined === null);
console.log( false === "0");
console.log( true === 1);

// The != Non-equality Operator
console.log(1 != 1); //if 1 is not equal to 1 return true 
console.log( 1 != "1");  // if 1 is not equal 1 return true 
console.log( "z" != "z"); 
console.log( true != true);
console.log( false != true); // if false is not equal to true return true
console.log( 0 != "");
console.log( undefined != null);
console.log( false != "0");
console.log( true != "1");

// The !== Strict non-equality operator cares about value and type 
